print("Welcome to the Salary Calculator!")
print("Imagine you're a hardworking adventurer in the Hourly Kingdom.")
print("You've been on a quest and need to calculate your earnings.")

hours = float(input("Enter the hours you adventured: "))
rate = float(input("Enter your hourly rate (in gold coins per hour): "))

salary = hours * rate
print(f"Congratulations, brave adventurer! You've earned {salary} gold coins for your quest.")

